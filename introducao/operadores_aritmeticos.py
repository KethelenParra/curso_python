# Introdução aos operadores aritméticos

adicao = 10 + 10
print('Adicao: ', adicao)

subtracao = 10 - 5
print('Subtracao: ', subtracao)

multiplicacao = 10 * 10
print('Multiplicacao: ', multiplicacao)

divisao = 10 / 5  # sempre o resultado vai ser float
print('Divisao: ', divisao)

divisao_inteira = 10 // 2
print('Divisao inteira: ', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciacao: ', exponenciacao)

modulo = 25 % 2   # resto da divisão
print('Módulo: ', modulo)