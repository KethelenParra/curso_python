"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> strign -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Kethelen Parra')
print('Kethelen "Souza"')

# Aspas duplas
print("Kethelen Souza")

# Escape
print("Kethelen \"Parra\"") # Kethelen "Parra"

# r
print(r"Kethelen \"Parra\"") # Kethelen \"Parra\"

print('explicito', 'é', 'melhor-que-implicito.', sep='-')
