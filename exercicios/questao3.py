numero1 = int(input('Digite o primeiro numero: '))
numero2 = int(input('Digite o segundo número: '))
numero3 = int(input('Digite o terceiro número: '))

a = numero1 + numero3
b = numero1 * numero2
c = numero2 - numero3

print(f'A soma do primeiro com o terceiro número é: {a}')
print(f'O produto do primeiro como segundo número é: {b}')
print(f'A diferença entre o segundo e o terceiro número é: {c}')